#!/usr/bin/env python3
"""
This module contains a function that converts a float to its string representation with type annotations.
"""


def to_str(n: float) -> str:
    """
    Return the string representation of a float.

    Args:
        n (float): The float number to convert to a string.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
